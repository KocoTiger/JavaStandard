package org.opentutorials.variables;

public class DoubleDemo {

	public static void main(String[] args) {
		double a = 1.1; 
		System.out.println(a+1.1); // 2.2
		 
		a = 2.1; 
		System.out.println(a+1.1); // 3.2
		
		//double 실수, 고로 a는 실수의 데이터에 담을 수 있다.
		//고로 int a = 1.1로 쓸경우, int는 정수만을 담을 수 있어 이클립스는 compile하지 않는다.
		//제한하지 않는 언어 : java script, php scrip언어들 대부분의 언어는 이런 것들을 엄격히 제한하지않아 추 후 문제가 될 수있다.
		
		
		

	}

}
