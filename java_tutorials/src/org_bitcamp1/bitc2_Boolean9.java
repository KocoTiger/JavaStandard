package org_bitcamp1;

public class bitc2_Boolean9 {

	public static void main(String[] args) {
	      
			int count = 9; //초기값 (initialized) <--배열길이가 10
	       
	       count += 1;                   // 출력할때 print전과 후가 다르다라는것 중요
	       System.out.println ( count ); // 처리하기, 처리를 기준으로 지금 카운트가 어떻게 되느냐! 변수를 먼저 증가후 처리!
	                                     // 배열의 기본 정의가 중요! / 정수의 기본정의!
	                                     // 초기값을 먼저 쓰고 해라! 어려우면
	        int n = 0;
	        //개수, 횟수 -> 계수한다.....
	        n += 1; //n = n + 1
	        System.out.println( n );
	        n -= 1; //n = n - 1
	        System.out.println( n );
	        
	        
	        
	        }                               
	}
