package org_bitcamp1;

public class bitc2_Boolean4 {

	public static void main(String[] args) {
        //논리 연산자 -> 관계식 여러 개 (관계연산자 여러개 있을때)
        
        boolean con1 = 10 < 20; //조건식 하나
        boolean con2 = 20 <= 30; // 20이랑 30이 같냐란 질문
         System.out.println(con1 && con2);        // 둘다
         System.out.println(con1 || con2);        // 둘다
 
        }                               
}
